package parte2;

public class Tripleta {
    private int p;
    private int mi;
    private int ma;

    public Tripleta(int p, int mi, int ma) {
        this.p = p;
        this.mi = mi;
        this.ma = ma;
    }

    public int getP() {
        return p;
    }

    public void setP(int p) {
        this.p = p;
    }

    public int getMi() {
        return mi;
    }

    public void setMi(int mi) {
        this.mi = mi;
    }

    public int getMa() {
        return ma;
    }

    public void setMa(int ma) {
        this.ma = ma;
    }
}
